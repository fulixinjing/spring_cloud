package cn.taskSys.entity;

public class Attendance{
	
	private String acId;
	private String acEmpCode;
	private String acgz;
	private String acStartTime;
	private String acEndTime;
	private String acgzFlag;
	
	
	public String getAcId() {
		return acId;
	}
	public void setAcId(String acId) {
		this.acId = acId;
	}
	public String getAcEmpCode() {
		return acEmpCode;
	}
	public void setAcEmpCode(String acEmpCode) {
		this.acEmpCode = acEmpCode;
	}
	public String getAcgz() {
		return acgz;
	}
	public void setAcgz(String acgz) {
		this.acgz = acgz;
	}
	public String getAcStartTime() {
		return acStartTime;
	}
	public void setAcStartTime(String acStartTime) {
		this.acStartTime = acStartTime;
	}
	public String getAcEndTime() {
		return acEndTime;
	}
	public void setAcEndTime(String acEndTime) {
		this.acEndTime = acEndTime;
	}
	public String getAcgzFlag() {
		return acgzFlag;
	}
	public void setAcgzFlag(String acgzFlag) {
		this.acgzFlag = acgzFlag;
	}
	
}